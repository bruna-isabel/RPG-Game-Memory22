map::map(SDL_Renderer* ren)
{
    grass = textureCreator::loadTexture("mossover.png", ren);
    
    loadMap(field);
    
    src_rect.x = src_rect.y = 0;
    src_rect.w = src_rect.h = dest_rect.w = dest_rect.h = 32;
}

void map::loadMap(int array[32][32])
{
    for(int row=0; row<32; row++){
        for(int col=0; col<32; col++){
          
        Map[row][col] = field[row][col];
        }
    }
}

void map::drawMap(SDL_Renderer* ren)
{
    int type = 0;
    
    for(int row=0; row<32; row++)
    {
        for(int col=0; col<32; col++)
        {
            type = Map[row][col];
        dest_rect.x = col * 32;
            dest_rect.y = row * 32;
            
            switch(type)
            {
                case 0:
                    textureCreator::draw(ren, grass, src_rect, dest_rect);
                default:
                    break;
            }
        }
    }
}
